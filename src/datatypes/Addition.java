package datatypes;

public class Addition {
	
	
	public static void main(String[] args) {
		
		int i = 10;
		
		int j = 20;
		
		int k = i+j;
		
		System.out.println(k);//30
		
		
//		Assignment - Write a Program to perform substraction and multiplication in java
		
		// substraction
		
		int l = 60;
		
		int m = 10;
		
		int n = l-m;
		
		System.out.println(n);//50
		
		
//		Multiplication
		
		int z = 20;
		
		int y = 80;
		
		int x = z*y;
		
		System.out.println(x);//1600
		
//		Division
		
		double e = 100000000;
		
		double f = 565656;
		
		double g = e/f;
		
		System.out.println(g);//3.333333335
		
	}

}
